package com.core.JavaPractise;

public class Variable_Example1 {

	public static void main(String[] args) {
		int num1 =12;
		int num2 =13;
		int num3 =11;
		int sum_result = (num1 + num2 + num3);
		int sub_result = (num1 - num2 - num3);
		int multi_result = (num1 * num2 * num3);
		int avg_result = (num1 + num2 + num3)/3;

		System.out.println("Sum of three numbers is : " + sum_result);
		System.out.println("Substraction of three numbers is : " + sub_result);
		System.out.println("Multiplication of three numbers is : " + multi_result);
		System.out.println("Average of three numbers is : " + avg_result);
	}
}
